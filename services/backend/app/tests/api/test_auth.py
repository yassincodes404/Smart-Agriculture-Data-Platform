class TestMe:
    def test_success_returns_200(self, client):
        """GET /me with valid token must return 200."""
        token = register_and_login(client)
        response = client.get(ME_URL, headers={"Authorization": f"Bearer {token}"})
        assert response.status_code == 200

    def test_success_returns_user_data(self, client):
        """Response must contain the authenticated user's info."""
        token = register_and_login(client)
        response = client.get(ME_URL, headers={"Authorization": f"Bearer {token}"})
        data = response.json()["data"]
        assert data["email"] == VALID_USER["email"]
        assert "user_id" in data
        assert "role" in data

    def test_no_token_returns_401(self, client):
        """Request without Authorization header must return 401."""
        response = client.get(ME_URL)
        assert response.status_code == 401

    def test_invalid_token_returns_401(self, client):
        """Tampered or garbage token must return 401."""
        response = client.get(ME_URL, headers={"Authorization": "Bearer invalidtoken.abc.def"})
        assert response.status_code == 401

    def test_expired_token_returns_401(self, client):
        """An expired token (manually crafted) must be rejected."""
        from datetime import timedelta
        from app.core.security import create_access_token
        expired_token = create_access_token(
            data={"sub": "9999"},
            expires_delta=timedelta(seconds=-1),  # already expired
        )
        response = client.get(ME_URL, headers={"Authorization": f"Bearer {expired_token}"})
        assert response.status_code == 401

    def test_password_not_in_me_response(self, client):
        """Password hash must never appear in /me response."""
        token = register_and_login(client)
        response = client.get(ME_URL, headers={"Authorization": f"Bearer {token}"})
        assert "password" not in response.text
        assert "hash" not in response.text


