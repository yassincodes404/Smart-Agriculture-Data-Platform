class TestLogin:
    def test_success_returns_200(self, client):
        """Successful login returns HTTP 200."""
        client.post(REGISTER_URL, json=VALID_USER)
        response = client.post(LOGIN_URL, json={
            "email": VALID_USER["email"],
            "password": VALID_USER["password"],
        })
        assert response.status_code == 200

    def test_success_returns_token(self, client):
        """Login response must contain access_token and token_type."""
        client.post(REGISTER_URL, json=VALID_USER)
        response = client.post(LOGIN_URL, json={
            "email": VALID_USER["email"],
            "password": VALID_USER["password"],
        })
        data = response.json()["data"]
        assert "access_token" in data
        assert data["token_type"] == "bearer"

    def test_token_is_non_empty_string(self, client):
        """Access token must be a non-empty string."""
        token = register_and_login(client)
        assert isinstance(token, str)
        assert len(token) > 0

    def test_wrong_password_returns_401(self, client):
        """Wrong password must return 401 Unauthorized."""
        client.post(REGISTER_URL, json=VALID_USER)
        response = client.post(LOGIN_URL, json={
            "email": VALID_USER["email"],
            "password": "wrongpassword",
        })
        assert response.status_code == 401

    def test_wrong_email_returns_401(self, client):
        """Non-existent email must return 401 Unauthorized."""
        response = client.post(LOGIN_URL, json={
            "email": "nobody@agri.com",
            "password": "whatever123",
        })
        assert response.status_code == 401

    def test_missing_fields_returns_422(self, client):
        """Login with empty body must fail validation."""
        response = client.post(LOGIN_URL, json={})
        assert response.status_code == 422

    def test_invalid_email_format_returns_422(self, client):
        """Login with malformed email must fail Pydantic validation."""
        response = client.post(LOGIN_URL, json={
            "email": "bad-email",
            "password": "password123",
        })
        assert response.status_code == 422


